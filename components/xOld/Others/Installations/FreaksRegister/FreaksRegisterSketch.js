export default function sketch(p) {
  var v = [];
  var x = 0;
  var targetx = 2;
  var padding = 80;
  var widthFactor = 0.8;
  var incr = 1;

  const ASPECT_RATIO = 0.5625;
  const TOTAL_IMG = 57;

  p.preload = function () {
    for (var i = 0; i < TOTAL_IMG; i++) {
      v[i] = p.loadImage(images[i]);
    }
  };

  p.setup = function () {
    let width = parseInt(window.innerWidth * widthFactor) - padding * 3;
    let height = window.innerHeight - padding * 2;

    if (window.innerWidth < 900) {
      width = window.innerWidth - padding * 2;
      height = width * ASPECT_RATIO;
    }

    p.createCanvas(width, height);
  };

  p.myCustomRedrawAccordingToNewPropsHandler = function (props) {
    if (props.padding !== null) padding = props.padding;
    if (props.width !== null) widthFactor = props.width;

    let width = parseInt(window.innerWidth * widthFactor) - padding * 3;
    let height = window.innerHeight - padding * 2;

    if (window.innerWidth < 900) {
      width = window.innerWidth - padding * 2;
      height = width * ASPECT_RATIO;
    }

    p.resizeCanvas(width, height);
  };

  p.windowResized = function () {
    let width = parseInt(window.innerWidth * widthFactor) - padding * 3;
    let height = window.innerHeight - padding * 2;

    if (window.innerWidth < 900) {
      width = window.innerWidth - padding * 2;
      height = width * ASPECT_RATIO;
    }

    p.resizeCanvas(width, height);
  };

  p.draw = function () {
    targetx = p.floor(p.map(p.mouseX, 0, p.width, TOTAL_IMG, 0));
    if (targetx >= TOTAL_IMG) targetx = TOTAL_IMG;
    if (targetx <= 1) targetx = 1;
    if (x === targetx) return;

    if (x - targetx < 0) incr = 1;
    else incr = -1;

    if (x !== targetx) x += incr;

    if (v[x]) p.image(v[x], 0, 0, p.width, p.height);
  };
}
